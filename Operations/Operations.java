public class Operations
{
	public static void main(String[] args)
	{
		int add = 60+40;
		int sub = 400 - 60;
		int mult = 1*30;
		int div = 30/6;
		int rem = 130%100;
		
		System.out.println("Adding two numbers: " + add);
		System.out.println("Subtracting two numbers: " + sub);
		System.out.println("Multiplying two numbers: " + mult);
		System.out.println("Dividing two numbers: " + div);
		System.out.println("Remainder of two numbers: " + rem);
	}
}